    private void abastecimentoCadastroBotao_botaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abastecimentoCadastroBotao_botaoActionPerformed
        // criando variaveis para alimentar o objeto Abastecimento a ser adicionado no cadastroDeAbastecimentos do objeto Carro
        double odometro = Double.parseDouble(abastecimentoCadastroOdometro_odometro.getText());
        String tipoDeCombustivel = abastecimentoCadastroTipoDeCombustivel_combustivel.getSelectedItem().toString();
        double quantidadeAbastecida = Double.parseDouble(abastecimentoCadastroQuantidadeAbastecida_quantidade.getText());
        double valorAbastecido = Double.parseDouble(abastecimentoCadastroValorAbastecido_valor.getText());
        boolean inicioDeSerie = abastecimentoCadastroInicioDeSerie_checkbox.isSelected();
        int dia = Integer.parseInt(AbastecimentoCadastroData_dia.getText());
        int mes = Integer.parseInt(AbastecimentoCadastroData_mes.getText());
        int ano = Integer.parseInt(AbastecimentoCadastroData_ano.getText());
        int hora = Integer.parseInt(AbastecimentoCadastroData_hora.getText());
        int minuto = Integer.parseInt(AbastecimentoCadastroData_min.getText());        
        Date diaDoAbastecimento = new Date(ano, mes, dia, hora, minuto);
        double precoLitro = valorAbastecido/quantidadeAbastecida;
        
        //alimentando o abastecimento
        negocio.Abastecimento abastecimento = new negocio.Abastecimento(tipoDeCombustivel, diaDoAbastecimento, odometro, quantidadeAbastecida, valorAbastecido, precoLitro, inicioDeSerie);
        int linha = abastecimentoCadastroResultado_table.getSelectedRow();
        String placa = 	abastecimentoCadastroResultado_table.getValueAt(linha,0).toString();
        DefaultTableModel model = (DefaultTableModel) abastecimentoCadastroResultado_table.getModel();       
        Carro carro = facade.pesquisaPlaca(placa);
        carro.getHistoricoDeAbastecimentos().add(abastecimento);
                
    }//GEN-LAST:event_abastecimentoCadastroBotao_botaoActionPerformed

